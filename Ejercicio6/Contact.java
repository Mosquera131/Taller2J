public class Contact {
    String  name;
    String phone;
    String email;

    public Contact()
    {
        this.name= name;
        this.phone= phone;
        this.email= email;

    }

    //getters
    public String Getname()
    {
        return name;
    }
    public String GetPhone()
    {
        return phone;
    }

    public String GetEmail()
    {
        return email;
    }

    //setters

    public void setName(String name)
    {
        this.name=name;
    }

    public void setPhone(String phone)
    {
        this.phone=phone;
    }

    public void setEmail(String email)
    {
        this.email=email;
    }

}
